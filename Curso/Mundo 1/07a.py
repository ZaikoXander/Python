# operadores aritméticos
#
# + adição
# - subtração
# * multiplicação
# / divisão
# ** potência
# // divisão inteira
# % resto da divisão
#
# = recebe
# == igual
#
# ordem de precedência
#
# 1- ()
# 2- **
# 3- * / // %
# 4- + -
#
print('5 + 2 vale:', 5 + 2)
print('5 - 2 vale:', 5 - 2)
print('5 x 2 vale:', 5 * 2)
print('5 / 2 vale:', 5 / 2)
print('5 ** 2 vale:', 5 ** 2)
print('5 // 2 vale:', 5 // 2)
print('5 % 2 vale:', 5 % 2)
